package com.example.blogkulinarnymobileapp.Adapters;

public class TileData {
    private String title;
    private String desc;

    public TileData(String title, String desc) {
        this.title = title;
        this.desc = desc;
    }

    public String getTitle() {
        return title;
    }

    public String getDesc() {
        return desc;
    }
}

